package Loops;
//Write a java program to print 1 to 20 numbers using the while loop.
public class Solution2 
{
    public static void main(String[] args) 
    {
        int i=1;
        while (i<=20)
        {
            System.out.print(i+" ");
            i=i+1;//or you can write i=i+1
        }

    }   
}
